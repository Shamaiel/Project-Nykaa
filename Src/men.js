  function sortByNames() {
    var value = document.getElementById("select-name").value;

    if (value === "ascending") {
     mensProducts.sort((a, b) => {
        if (a.desc > b.desc) {
          return 1;
        }
        if (a.desc < b.desc) {
          return -1;
        }
        return 0;
      })

    } 
    if (value === "descending") {
      mensProducts.sort((a, b) => {
        if (a.desc > b.desc) {
          return -1;
        }
        if (a.desc < b.desc) {
          return 1;
        }
        return 0;
      })
    } 
    
   showMenSection(mensProducts)

  }
 

